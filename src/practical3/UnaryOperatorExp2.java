package practical3;

public class UnaryOperatorExp2 {
    public static void main(String[] args) {
        int a = 25;
        int b = -35;
        boolean c = true;
        boolean d = false;

        System.out.println(~a);
        System.out.println(~b);

        System.out.println(!c);
        System.out.println(!d);
    }
}
