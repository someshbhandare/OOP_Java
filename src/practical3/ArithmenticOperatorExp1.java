package practical3;

public class ArithmenticOperatorExp1 {
    public static void main(String[] args) {
        int a = 30;
        int b = 15;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(10*10/5+3-1*4/2);
    }
}
